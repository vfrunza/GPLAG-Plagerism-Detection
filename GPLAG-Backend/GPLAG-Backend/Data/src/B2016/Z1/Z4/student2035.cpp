#include <iostream>
#include <string>
#include <vector>


void ObrniFraze(std::string recenica, std::vector<std::string> fraze) {
	for(unsigned int k=0; k<fraze.size(); k++) {
	for(unsigned int j=0; j<fraze[k].size(); j++) {
		for(unsigned int i=0; i<recenica.size(); i++)
		{
			
			if(i==recenica.size()-1) break;
		}
		
		
		if(j==fraze[k].size()-1) break;
	} if(k==fraze.size()-1) break;
}
}
